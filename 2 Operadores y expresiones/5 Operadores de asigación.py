# Operador de asigación tradicional
a = 0

# Suma en asignación
# Suponiendo que la variable a vale 0(declarada arriba), podemos directamente sumarle un valor... asi
# Nota: tener en cuenta que la variable a sumar ya debe estar declarda antes porque o sino dira que no existe
a += 1
print(a)

# Resta en asignación
a = 15
a -= 10
print(a)

# Multiplicación en asignación
a = 5
a *= 2
print(a)

# División en asignación
a = 10
a /= 2
print(a)

# Modulo en asignación
a = 5
a %= 2
print(a)

# Potencia en asignación
a = 4
a **= 6
print(a)

